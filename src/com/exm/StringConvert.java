package com.exm;

public class StringConvert {

	public static void main(String[] args) {
		
	
		int x = Integer.valueOf("1000");
		long l = Long.valueOf("1000000000000000000");
		
		float f = Float.valueOf("100.3");
		double d = Double.valueOf("1000000000000000000.23");
		
		
		
		int k = 'x'; //ASCII value of x 
		
		System.out.println(k);
		
		System.out.println(x);
		System.out.println(l);
		
		System.out.println(f);
		System.out.println(d);
		
		
		


	}

}
